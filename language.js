// Current language state
let currentLanguage = 'en';

// Initialize language support
function initializeLanguageSupport() {
    // Load saved language preference
    const savedLanguage = localStorage.getItem('jharkhandExplorerLanguage') || 'en';
    setLanguage(savedLanguage);
    
    // Set up language switcher
    setupLanguageSwitcher();
    
    console.log('üåê Language support initialized');
}

// Set up language switcher functionality
function setupLanguageSwitcher() {
    const langSwitcher = document.getElementById('langSwitcher');
    if (!langSwitcher) return;
    
    // Create language dropdown
    createLanguageDropdown();
    
    // Add click event to toggle dropdown
    langSwitcher.addEventListener('click', (e) => {
        e.stopPropagation();
        toggleLanguageDropdown();
    });
    
    // Close dropdown when clicking outside
    document.addEventListener('click', (e) => {
        if (!langSwitcher.contains(e.target)) {
            closeLanguageDropdown();
        }
    });
}

// Create language dropdown
function createLanguageDropdown() {
    const langSwitcher = document.getElementById('langSwitcher');
    const dropdown = document.createElement('div');
    dropdown.className = 'language-selector';
    dropdown.id = 'languageDropdown';
    
    // Create options for each language
    Object.values(LanguageData).forEach(lang => {
        const option = document.createElement('div');
        option.className = `language-option ${lang.code === currentLanguage ? 'active' : ''}`;
        option.innerHTML = `
            <span>${lang.flag}</span>
            <span>${lang.name}</span>
        `;
        option.addEventListener('click', () => {
            setLanguage(lang.code);
            closeLanguageDropdown();
        });
        dropdown.appendChild(option);
    });
    
    langSwitcher.appendChild(dropdown);
}

// Toggle language dropdown
function toggleLanguageDropdown() {
    const dropdown = document.getElementById('languageDropdown');
    if (dropdown) {
        dropdown.classList.toggle('show');
    }
}

// Close language dropdown
function closeLanguageDropdown() {
    const dropdown = document.getElementById('languageDropdown');
    if (dropdown) {
        dropdown.classList.remove('show');
    }
}

// Set language and update UI
function setLanguage(langCode) {
    if (!LanguageData[langCode]) {
        console.warn(`Language ${langCode} not found, defaulting to English`);
        langCode = 'en';
    }
    
    currentLanguage = langCode;
    const langData = LanguageData[langCode];
    
    // Update language switcher button
    updateLanguageSwitcherButton(langData);
    
    // Update all text content
    updatePageContent(langData.content);
    
    // Update active option in dropdown
    updateLanguageDropdownActive(langCode);
    
    // Save preference
    localStorage.setItem('jharkhandExplorerLanguage', langCode);
    
    // Update document language attribute
    document.documentElement.lang = langCode;
    
    console.log(`üåê Language changed to: ${langData.name}`);
}

// Update language switcher button
function updateLanguageSwitcherButton(langData) {
    const langSwitcher = document.getElementById('langSwitcher');
    if (langSwitcher) {
        langSwitcher.innerHTML = `<i class="fas fa-globe"></i> ${langData.code.toUpperCase()}`;
    }
}

// Update language dropdown active state
function updateLanguageDropdownActive(langCode) {
    const options = document.querySelectorAll('.language-option');
    options.forEach(option => {
        option.classList.remove('active');
        if (option.innerHTML.includes(LanguageData[langCode].name)) {
            option.classList.add('active');
        }
    });
}

