// Check if RTL language (for future Arabic/Urdu support)
function isRTLLanguage(langCode = currentLanguage) {
    const rtlLanguages = ['ar', 'ur', 'fa']; // Arabic, Urdu, Persian
    return rtlLanguages.includes(langCode);
}

